package pl.krepam.Game;

public class Position{
	private short x;
	private short y;
	public Position(short x, short y) {
		super();
		this.x = x;
		this.y = y;
	}
	public short getX() {
		return x;
	}
	public void setX(short x) {
		this.x = x;
	}
	public short getY() {
		return y;
	}
	public void setY(short y) {
		this.y = y;
	}
	
	
}